package quelacanaria;

public class f017_nestedLoop {

    public static void main(String[] args) {
        //nested loop = a loop inside another loop
        //Used often with matrices or DS&A
        for(int h = 0; h <= 3; h++) {
            for (int i = 0; i <= 10; i++) {
                System.out.print(i + " ");
            }
            System.out.println();
        }

    }

}
